            <p>Some people know how to exercise and they even know how to do the exercises correctly, but they like having a trainer around for support and supervision. If you're lifting very heavy weights or need someone to help with partner-type exercises, working with a trainer might be a good choice for you. He can spot you during workouts and help you come up with a good training plan for your goals.</p>
