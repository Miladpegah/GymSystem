            <h2>Why do you need a personal trainer? </h2>
            <p>If you've been exercising consistently for several weeks or months and aren't losing weight or reaching your goals, hiring a trainer may be a good choice. A trainer can look at your current program and eating habits and help you see where you could make changes to create more effective workouts. A trainer can also help you determine if the goals you've set are realistic for you, hold you accountable for your workouts and help you stay motivated to exercise.  You may even find that you are getting results, just not in the way you expected, something a trainer may see more clearly from the outside.</p>
            <p>If you're an experienced exerciser, maybe you haven't considered working with a personal trainer. However, it can be a great choice if you need some variety in your workouts. A trainer can bring a fresh perspecitve and new ideas to challenge both your body and your mind. Even if you just do a few sessions or meet every few weeks, you'll find it refreshing to have new workouts and new exercise toys to play with.</p>
